class Main {
  public static void main(String[] args) {
    int number = 15;
    int number2 = 5;

    // Greater than
    System.out.println(number > number2);
    // less than
    System.out.println(number < number2);
    // not equal
    System.out.println(number != number2);
    // less than or equal
    System.out.println(number <= number2);
    // greater than or equal
    System.out.println(number >= number2);
  }
}
